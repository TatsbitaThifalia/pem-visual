a = 20
b = 9

print('Nilai dari operasi bitwise "AND" antara ',a, ':,',b,':' '-> (a and b) =', a & b )
print('Nilai dari operasi bitwise "OR" antara ',a, ':,',b,':' '-> (a or b) =', a | b )
print('Nilai dari operasi bitwise "XOR" antara ',a, ':,',b,':' '-> (a xor b) =', a ^ b )